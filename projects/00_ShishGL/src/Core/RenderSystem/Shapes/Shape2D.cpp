/*============================================================================*/
#include "Shape2D.hpp"
/*============================================================================*/
using namespace ShishGL;
/*============================================================================*/

Shape2D::Shape2D(const Vector2<double> &pos)
    : s_pos(pos)
    { }

/*----------------------------------------------------------------------------*/

const Vector2<double>& Shape2D::getPos() const {
    return s_pos;
}

/*----------------------------------------------------------------------------*/

void Shape2D::setPos(const Vector2<double>& pos) {
    s_pos = pos;
}

/*----------------------------------------------------------------------------*/

void Shape2D::translate(const Vector2<double>& delta) {
    s_pos += delta;
}

