/*============================================================================*/
#include "Object.hpp"
#include "SubscriptionManager.hpp"
/*============================================================================*/
using namespace ShishGL;
/*============================================================================*/

Object::Object(Object::ID id)
    : obj_id(id)
    { }

/*----------------------------------------------------------------------------*/

Object::~Object() {
    SubscriptionManager::unsubscribeAll(obj_id);
    SubscriptionManager::unsubscribeFromAll(obj_id);
}

/*----------------------------------------------------------------------------*/

Object::ID Object::id() const {
    return obj_id;
}

/*----------------------------------------------------------------------------*/

bool Object::filterEvent(Event&) const {
    return true;
}

/*----------------------------------------------------------------------------*/

bool Object::onEvent(Event&) {
    return false;
}

